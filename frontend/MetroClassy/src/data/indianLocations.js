// Get all cities
export const INDIAN_CITIES = Object.keys(CITY_TO_STATE).sort();

// Helper function to get state from city
export const getStateFromCity = (city) => {
    return CITY_TO_STATE[city] || '';
};

// Helper function to search cities with optional state filter
export const searchCities = (query, state = null) => {
    if (!query) return [];
    const lowerQuery = query.toLowerCase();
    let filteredCities = INDIAN_CITIES;
    
    // Filter by state if provided
    if (state) {
        filteredCities = INDIAN_CITIES.filter(city => 
            CITY_TO_STATE[city] === state
        );
    }
    
    return filteredCities.filter(city =>
        city.toLowerCase().includes(lowerQuery)
    ).slice(0, 10); // Return top 10 matches
};

// Get all cities for a specific state
export const getCitiesByState = (state) => {
    if (!state) return [];
    return INDIAN_CITIES.filter(city => 
        CITY_TO_STATE[city] === state
    ).sort();
};
